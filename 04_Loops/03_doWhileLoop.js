// ==========================================
// DO...WHILE LOOP
// ==========================================
// A do...while loop executes the code at least once before checking the condition.

// Syntax:
// do {
//     // code
// } while (condition);

// Example: Print numbers from 0 to 2

let i = 0;

do {
    console.log(i);
    i++;
} while (i < 3);


// ==========================================
// ANOTHER EXAMPLE
// ==========================================
// Print numbers from 1 to 5

let num = 1;

do {
    console.log(num);
    num++;
} while (num <= 5);


