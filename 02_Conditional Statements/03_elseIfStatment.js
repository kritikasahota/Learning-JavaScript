// ======================================
// JavaScript else if Statement (Notes)
// ======================================

// The else if statement is used to check multiple conditions one after another.
//It is executed only if the previous condition is false.

// Syntax:
// if (condition1) {
//     // Executes if condition1 is true
// } else if (condition2) {
//     // Executes if condition2 is true
// } else {
//     // Executes if all conditions are false
// }


// ======================================
// Example: Age Category
// ======================================

let age = 25;

if (age <= 18) {
    console.log("Junior");
}
else if (age >= 60) {
    console.log("Senior");
}
else {
    console.log("Middle");
}




