//Print price of popcorn according to its size

let size = "XXL";

if( size === "XL"){
    console.log("Price is Rs.250")
}else if( size === "L"){
    console.log("Price is Rs.200")
}else if( size === "M"){
    console.log("Price is Rs.100")
}else if( size === "S"){
    console.log("Price is Rs.50")
}else{
    console.log("This size doesn't exist.")
}
